public class BubbleSort
{

    public  void bubbleSort( int[] arr )
    {
        int n = arr.length;
        boolean swapped;
        for (int i = 0; i < n - 1; i++) {
            swapped = false;
            if (!swapped) {
                break;
            }
        }
    }

}
